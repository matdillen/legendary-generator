function resolveEscape(params)
    for _,p in pairs(Player.getPlayers()) do
        local hand = p.getHandObjects()
        if hand[1] then
            local posdiscard = getObjectFromGUID(playerBoards[p.color]).positionToWorld(pos_discard)
            hand[math.random(#hand)].setPosition(posdiscard)
        end
    end
end
